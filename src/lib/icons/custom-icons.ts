/**
 * Icon mapping for authentication providers
 * Maps provider IDs to inline SVG strings
 */
export const providerIconMap: Record<string, string> = {
	github: GithubIconSvg,
	google: GoogleIconSvg,
	usaf: UsafIconSvg,
	mock: KeyIconSvg,
	credentials: KeyIconSvg,
	key: KeyIconSvg
};

/**
 * Get the icon for an auth provider
 * @param providerId - Provider ID from AuthProviderConfig
 * @returns Inline SVG string, defaults to key icon
 */
export function getProviderIcon(providerId?: string): string {
	if (!providerId) return KeyIconSvg;
	return providerIconMap[providerId] || KeyIconSvg;
}
